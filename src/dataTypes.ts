// Basic Data Types:

// 1. Primitive Types:
// String
let firstName: string = "Akhtar";

// Number
let age: number = 25;

// Boolean
let isActive: boolean = true;

// Undefined
let x: undefined = undefined;

// Null
let y: null = null;

// Inferred
let z;

// Any
let hobby: any = ["A", "B", "C", "D", "E"];
hobby = "Gardening";
hobby = undefined;

// 2. Non primitive types
// Array
let students: string[] = ["Rachel", "Monica", "Chandler"];
let roll: number[] = [2, 4, 5];

// Tuple
let coordinates: [number, number] = [1, 5];
let teacher: [number, string, boolean] = [34, "Mark", true];
